def _get_general_mode_rules(response_mode: str) -> str:
    mode = (response_mode or "detailed").strip().lower()

    if mode == "short":
        return """
Правила:
- Пиши по-русски.
- Дай очень короткий ответ.
- Используй 1-2 предложения.
- Объясни только суть документа.
- Если данных недостаточно, напиши: "В предоставленных фрагментах недостаточно данных для краткого резюме документа."
""".strip()

    if mode == "expert":
        return """
Правила:
- Пиши по-русски.
- Дай структурный и более точный ответ.
- Используй 4-6 предложений.
- Укажи, что это за документ, между кем он заключён, что регулирует, какие права/обязанности или ограничения видны из фрагментов.
- Не выдумывай юридические детали, которых нет в тексте.
- Если данных недостаточно, напиши: "В предоставленных фрагментах недостаточно данных для полного экспертного резюме документа."
""".strip()

    return """
Правила:
- Пиши по-русски.
- Дай краткое и понятное резюме.
- Используй 2-4 предложения.
- Объясни, что это за документ, между кем он заключён и что в целом регулирует.
- Если данных недостаточно, напиши: "В предоставленных фрагментах недостаточно данных для полного резюме документа."
""".strip()


def _get_specific_mode_rules(response_mode: str) -> str:
    mode = (response_mode or "detailed").strip().lower()

    if mode == "short":
        return """
Правила:
- Пиши по-русски.
- Дай только короткий прямой ответ.
- Без заголовков.
- Используй 1-2 предложения максимум.
- Если точного ответа нет, скажи: "В предоставленных фрагментах нет достаточно данных для точного ответа."
""".strip()

    if mode == "expert":
        return """
Правила:
- Пиши по-русски.
- Сначала дай прямой ответ.
- Затем добавь краткий разбор условий, ограничений, исключений или важных нюансов, если они есть во фрагментах.
- Пиши более формально и точно.
- Используй формат:

Краткий ответ:
...

Детали:
...

- Если точного ответа нет, скажи: "В предоставленных фрагментах нет достаточно данных для точного экспертного ответа."
""".strip()

    return """
Правила:
- Пиши по-русски.
- Пиши кратко, ясно и естественно.
- Сначала дай короткий прямой ответ.
- Затем, если нужно, добавь 1-3 предложения с пояснением.
- Используй формат:

Краткий ответ:
...

Пояснение:
...

- Если точного ответа нет, скажи: "В предоставленных фрагментах нет достаточно данных для точного ответа."
""".strip()